"""This module contain all of the operations available to realize some operation
on different numbers like addition, subtraction, multiplication and division"""

def addition(a: float, b: float) -> float:
    """
    Addition operation

    This function take two floats (work also with int) and return the result of 
    their addition.
    
    :param a: Float
    :param b: Float
    :rtype: Float
    """
    return a + b


def subtraction(a: float, b: float) -> float:
    """
    Subtraction operation

    This function take two floats (work also with int) and return the result of 
    their subtraction.
    
    :param a: Float
    :param b: Float
    :rtype: Float
    """
    return a - b

def multiplication(a: float, b: float) -> float:
    """
    Multiplication operation

    This function take two floats (work also with int) and return the result of 
    their multiplication.
    
    :param a: Float
    :param b: Float
    :rtype: Float
    """
    return a * b

def division(a: float, b: float) -> float:
    """
    Division operation

    This function take two floats (work also with int) and return the result of 
    their division.
    
    :param a: Float
    :param b: Float
    :rtype: Float
    """
    return a / b
